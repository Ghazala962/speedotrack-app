    super.initState();
    _model = createModel(context, () => PlaybackLoadingModel());

    // On page load action.
    SchedulerBinding.instance.addPostFrameCallback((_) async {
      _model.playbackDataRes =
          await NewSpeedotrackJSApiGroup.playbackAPICall.call(
        imei: widget.deviceImei,
        dtf: widget.startDate,
        dtt: widget.endDate,
        minStopDuration: '10',
        jwt: FFAppState().userJwt,
      );

      if ((_model.playbackDataRes?.succeeded ?? true)) {
        if (Navigator.of(context).canPop()) {
          context.pop();
        }
        context.pushNamed(
          PlaybackFinalWidget.routeName,
          queryParameters: {
            'playbackHistoryData': serializeParam(
              (_model.playbackDataRes?.jsonBody ?? ''),
              ParamType.JSON,
            ),
            'deviceData': serializeParam(
              widget.deviceData,
              ParamType.JSON,
            ),
          }.withoutNulls,
          extra: <String, dynamic>{
            kTransitionInfoKey: TransitionInfo(
              hasTransition: true,
              transitionType: PageTransitionType.fade,
              duration: Duration(milliseconds: 0),
            ),
          },
        );
      } else {
        ScaffoldMessenger.of(context).showSnackBar(
          SnackBar(
            content: Text(
              getJsonField(
                (_model.playbackDataRes?.jsonBody ?? ''),
                r'''$.message''',
              ).toString().toString(),
              style: TextStyle(
                color: FlutterFlowTheme.of(context).secondaryBackground,
              ),
            ),
            duration: Duration(milliseconds: 4000),
            backgroundColor: FlutterFlowTheme.of(context).error,
          ),
        );
      }
    });

    WidgetsBinding.instance.addPostFrameCallback((_) => safeSetState(() {}));
  }

